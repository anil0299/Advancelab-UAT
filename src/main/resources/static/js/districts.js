  function loadDistricts() {
  const stateInput = document.getElementById("State");
  const districtSelect = document.getElementById("district");

  // Get the selected state
  const selectedState = stateInput.value.trim();

  // Find the districts for the selected state
  const stateData = jsonData.find(state => state.state === selectedState);

  // Clear previous options
  districtSelect.innerHTML = "";

  // Create and append default option for a placeholder
  const defaultOption = document.createElement("option");
  defaultOption.value = "";
  defaultOption.textContent = "Select district";
  districtSelect.appendChild(defaultOption);

  // Create and append new options
  if (stateData && stateData.districts) {
    stateData.districts.forEach(district => {
      const option = document.createElement("option");
      option.value = district;
      option.textContent = district;
      districtSelect.appendChild(option);
    });
  } else {
    // If no districts found, create and append a corresponding option
    const option = document.createElement("option");
    option.textContent = "No districts found";
    districtSelect.appendChild(option);
  }
}